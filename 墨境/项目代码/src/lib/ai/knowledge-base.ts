/**
 * 知识库分类
 */
export type KnowledgeCategory =
  | 'iron_rules'      // 核心铁律+防守规则（L1）
  | 'techniques'      // 核心工具箱（场景/收束/钩子/感官/信息/情感/黄金三章等）
  | 'polish'          // 语言净化与润色规则（禁用词/四级净化/替换表/精修质检）
  | 'genre_params'    // 题材参数与风格指南（题材映射/冲突分级/口语化规范）
  | 'narrative'       // 叙事结构与角色工具（开场/人设/大纲/救猫咪/英雄之旅/跨书技法/深度感）
  | 'operations'      // 系统运维与状态管理（冷却/伏笔/角色成长/卡文/指令索引）

/**
 * 注入时机
 */
export type InjectionTiming =
  | 'always'          // 每次 AI 调用
  | 'planning'        // 规划阶段
  | 'writing'         // 写作阶段
  | 'chapter_end'     // 章末自检
  | 'polish'          // 润色时
  | 'first_three'     // 仅前三章
  | 'on_demand'       // 按需 RAG 检索

/**
 * 单条知识条目
 */
export interface KnowledgeItem {
  /** 唯一标识，如 'technique_A_S1' */
  id: string
  /** 所属分类 */
  category: KnowledgeCategory
  /** 条目标题 */
  title: string
  /** 完整规则文本（保留中文原文） */
  content: string
  /** 检索关键词标签 */
  tags: string[]
  /** 注入时机 */
  timing: InjectionTiming
  /** 优先级 1-5，越高越优先注入 */
  priority: number
}

