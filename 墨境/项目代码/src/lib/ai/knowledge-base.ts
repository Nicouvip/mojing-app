/**
 * 按注入时机获取知识条目
 */
export function getKnowledgeByTiming(timing: InjectionTiming): KnowledgeItem[] {
  return KNOWLEDGE_BASE.filter(item => item.timing === timing)
}

/**
 * 按标签检索知识条目（标签匹配）
 */
export function searchKnowledgeByTags(tags: string[]): KnowledgeItem[] {
  const tagSet = new Set(tags.map(t => t.toLowerCase()))
  return KNOWLEDGE_BASE.filter(item =>
    item.tags.some(t => tagSet.has(t.toLowerCase()))
  )
}

/**
 * 按分类获取知识条目
 */
export function getKnowledgeByCategory(category: KnowledgeCategory): KnowledgeItem[] {
  return KNOWLEDGE_BASE.filter(item => item.category === category)
}

/**
 * 获取所有"always"时机的条目（每次AI调用都注入）
 */
export function getAllAlwaysItems(): KnowledgeItem[] {
  return KNOWLEDGE_BASE.filter(item => item.timing === 'always')
}

/**
 * 注入格式化：把条目列表转成 prompt 中可用的文本
 */
export function formatKnowledgeForPrompt(items: KnowledgeItem[]): string {
  return items
    .sort((a, b) => b.priority - a.priority)
    .map(item =>
      `【知识库·${item.category}】${item.title}\n${item.content}`
    )
    .join('\n\n')
}

/**
 * 按上下文检索最相关的知识条目
 */
export function retrieveRelevantKnowledge(options: {
  genre?: string
  conflictLevel?: string
  style?: string
  chapterIndex?: number
  lastReport?: {
    forbiddenA: number
    forbiddenB: number
    forbiddenC: number
    forbiddenD: number
  }
}): KnowledgeItem[] {
  const tags: string[] = []

  // 1. 根据写作上下文构建检索标签
  if (options.genre) tags.push(options.genre)
  if (options.conflictLevel) tags.push(options.conflictLevel.toUpperCase())
  if (options.style) tags.push(options.style)

  // 2. 从上章检测结果提取违规标签
  if (options.lastReport) {
    if (options.lastReport.forbiddenA > 0) tags.push('A类')
    if (options.lastReport.forbiddenB > 0) tags.push('B类')
    if (options.lastReport.forbiddenC > 0) tags.push('C类')
    if (options.lastReport.forbiddenD > 0) tags.push('D类')
  }

  // 3. 前三章特殊处理
  if (options.chapterIndex !== undefined && options.chapterIndex <= 3) {
    tags.push('前三章')
  }

  // 4. 检索
  const results = searchKnowledgeByTags(tags)

  // 5. 返回前5条，按优先级排序
  return results
    .sort((a, b) => b.priority - a.priority)
    .slice(0, 5)
}

